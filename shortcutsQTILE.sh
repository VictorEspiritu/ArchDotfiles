# mod --> window key 

mod + k              ----> swtich window to up
mod + j              ----> swtich window to down 
mod + h              ----> swtich window to left 
mod + l              ----> swtich window to right
mod + <shift> + k    ----> move window to up
mod + <shift> + j    ----> move window to down
mod + <shift> + h    ----> move window to left
mod + <shift> + l    ----> move window to right
mod + <ctrl> + k     ----> Grow window secondary
mod + <ctrl> + j     ----> Reduce window secondary
mod + <ctrl> + h     ----> Grow window main 
mod + <ctrl> + l     ----> Reduce window main
mod + <ctrl> + n     ----> Reset changes to windows
mod + <ctrl> + f     ----> Flip layout

mod + <space>        ----> switch nexxt window
mod + <tab>          ----> switch next layout 
mod + w              ----> close window 
mod + <ctrl> + r     ----> restart qtile with new  configuration
mod + <group>        ----> switch group name 
mod + <switch> + <group> --> send a window to that group 
mod + <enter>        ----> run a xterm or urxvt terminal 
mod + r              ----> start a litle prompt in the bar 
mod + d              ----> change current window to floating
mod + v              ----> current window fullscreen





